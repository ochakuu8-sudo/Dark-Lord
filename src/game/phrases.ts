// ── ヘルパー: 指定タイプのタグ一覧を取得 ──────────────────────
export function getPhraseTags(abilityType: string): Phrase[] {
    return PASSIVE_PHRASE_TAGS[abilityType] ?? [];
}

// ── ヘルパー: 指定タグを持つアビリティ一覧を取得（レリック判定用）──
export function getAbilitiesWithTag(tag: Phrase): string[] {
    return Object.entries(PASSIVE_PHRASE_TAGS)
        .filter(([, tags]) => tags.includes(tag))
        .map(([type]) => type);
}
